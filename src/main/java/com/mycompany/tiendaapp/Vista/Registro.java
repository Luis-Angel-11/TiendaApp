    void limpiar(){
        txtApellido.setText("");
        txtContra.setText("");
        txtTelefono.setText("");
        txtUsuario.setText("");
        txtNombre.setText("");
        txtDNI.setText("");
    }
    private boolean validarDNI(String dni){
        return dni.matches("\\d{8}");
    }
    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(!txtNombre.getText().isEmpty()&& !txtApellido.getText().isEmpty()&& !txtTelefono.getText().isEmpty()
           && !txtUsuario.getText().isEmpty() && !txtContra.getText().isEmpty()){
            if(!validarDNI(txtDNI.getText())){
                JOptionPane.showMessageDialog(this, "El DNI debe contener exactamente 8 d√≠gitos num√©ricos", "Advertencia", JOptionPane.WARNING_MESSAGE);
                return;
            }
            DaoClienteDB dao = new DaoClienteDB();
            Cliente cliente = new Cliente();
            
            cliente.setNombre(txtNombre.getText());
            cliente.setApellido(txtApellido.getText());
            cliente.setDni(txtDNI.getText());
            cliente.setTelefono(txtTelefono.getText());
            cliente.setUser(txtUsuario.getText());
            cliente.setPassword(txtContra.getText());
            
            cliente.setId(dao.generarId());
            String resultado = dao.Insert(cliente);
            if(resultado == null){
                JOptionPane.showMessageDialog(this, "Registro exitoso", "√âxito", JOptionPane.INFORMATION_MESSAGE);
                limpiar();
            }else {                           
                JOptionPane.showMessageDialog(this, "Error al registrar: " + resultado, "Error", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            
            JOptionPane.showMessageDialog(this, "Faltan llenar casilleros", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

